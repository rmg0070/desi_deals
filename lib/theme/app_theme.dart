  static const Color accentLight = Color(0xFFF97316); // Orange
  static const Color successLight = Color(0xFF22C55E); // Green
  static const Color dangerLight = Color(0xFFEF4444); // Red
  static const Color backgroundLight = Color(0xFFFAFAFA); // Warm white
  static const Color surfaceLight = Color(0xFFFFFFFF); // Pure white
  static const Color borderLight = Color(0xFFE2E8F0); // Light gray
